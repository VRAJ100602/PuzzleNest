class WordleGuessRequest(BaseModel):
    puzzle_id: str
    guess: str


@router.post("/guess")
def guess_wordle(req: WordleGuessRequest):
    target = get_solution(req.puzzle_id)
    if target is None:
        raise HTTPException(status_code=404, detail="Puzzle not found or expired")
    guess = req.guess.lower()
    if len(guess) != len(target):
        raise HTTPException(status_code=400, detail="Guess must be the same length as the target word")
    # Build per-letter feedback
    feedback = []
    target_chars = list(target)
    # First pass: mark correct positions
    for i, ch in enumerate(guess):
        if ch == target_chars[i]:
            feedback.append("correct")
            target_chars[i] = None  # consumed
        else:
            feedback.append(None)
    # Second pass: mark present/absent
    for i, ch in enumerate(guess):
        if feedback[i] is not None:
            continue
        if ch in target_chars:
            feedback[i] = "present"
            target_chars[target_chars.index(ch)] = None  # consumed
        else:
            feedback[i] = "absent"
    correct = all(f == "correct" for f in feedback)
    res_dict = {"feedback": feedback, "correct": correct}
    if correct:
        # Anti-Cheat: Validate elapsed solve time (minimum 3 seconds)
        gen_time = get_generation_time(req.puzzle_id)
        if gen_time:
            elapsed = time.time() - gen_time
            if elapsed < 3.0:
                raise HTTPException(status_code=400, detail="Solve time too fast. Suspicious activity detected.")
        solve_token = create_solve_token(req.puzzle_id, "wordle")
        res_dict["solve_token"] = solve_token
    return res_dict


