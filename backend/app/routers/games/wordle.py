import hashlib

def get_stable_index(seed_str: str, max_val: int) -> int:
    h = hashlib.sha256(seed_str.encode()).hexdigest()
    return int(h, 16) % max_val

@router.get("/new")
@_rate_limit("30/minute")
def get_new_wordle(
    request: Request,
    level: int = None
):
    word = random.choice(WORD_LIST)
    
    puzzle_id = store_solution(word)
    
    res = {
        "puzzle_id": puzzle_id,
        "valid_words": WORD_LIST  # To check guesses in the frontend
    }
    
    if level is not None:
        level = max(1, min(level, 100))
        if level <= 30:
            max_guesses = 6
        elif level <= 60:
            max_guesses = 5
        elif level <= 80:
            max_guesses = 4
        else:
            max_guesses = 3
        res["level"] = level
        res["max_guesses"] = max_guesses
        
    return res

